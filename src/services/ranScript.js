/**
 * buildWaMessages(context)
 * @param {{ name?: string, city?: string, propertyType?: string, customNote?: string }} context
 * @returns {{ initial: string, followup1: string, followup2: string }}
 *
 * initial   – הודעת פתיחה (נשלחת ב-launch)
 * followup1 – אם אין מענה אחרי wa_wait_minutes / 2  (אופציונלי)
 * followup2 – הודעה לפני שעוברים לשיחה (אופציונלי)
 */
function buildWaMessages({ name, city, propertyType, customNote } = {}) {
  const greeting = name ? `שלום ${name}` : 'שלום';
  const cityLine  = city ? ` ב${city}` : '';
  const propLine  = propertyType ? ` — ${propertyType}` : '';
  const extra     = customNote ? `\n\n${customNote}` : '';

  return {
    // ── הודעת פתיחה ──────────────────────────────────────────────
    initial: [
      `${greeting} 👋`,
      `אני רן מ-QUANTUM${cityLine}${propLine}.`,
      ``,
      `יש לנו מידע על נכסים באזור שלך שרוב האנשים אפילו לא יודעים שקיים — לפני שהם יוצאים לשוק.${extra}`,
      ``,
      `מתי נוח לך לדבר 2-3 דקות?`,
    ].join('\n'),

    // ── תזכורת 1 (אחרי חצי מזמן ההמתנה, אם יש) ─────────────────
    followup1: [
      `${greeting}, רן מ-QUANTUM שוב 🙂`,
      ``,
      `הייתי רוצה לשתף אותך בהזדמנות שזיהינו ב${city || 'האזור'} — משהו שרלוונטי ספציפית לנכסים כמו שלך.`,
      ``,
      `5 דקות שיחה יכולות לעשות הרבה. מתי מתאים?`,
    ].join('\n'),

    // ── הודעה לפני שיחה (נשלחת כשהמערכת עומדת להתקשר) ──────────
    followup2: [
      `${greeting}, רן מ-QUANTUM.`,
      ``,
      `ניסיתי ליצור קשר — כנראה לא נוח לך לכתוב כרגע.`,
      `אתקשר אליך ממספר QUANTUM בעוד כמה דקות.`,
      ``,
      `אם לא נוח — שלח לי "מאוחר יותר" ואמשיך ביום 🙂`,
    ].join('\n'),
  };
}

